export default function typeColor (type) {
    switch (type) {
        case 'grass':
            return '#78C84F'
        case 'water':
            return '#6790F0'
        case 'fire':
            return '#EF8031'
        case 'normal':
            return '#A8A877'
        case 'electric':
            return '#F9CF30'
        case 'ice':
            return '#99D8D8'
        case 'fighting':
            return '#C03028'
        case 'poison':
            return '#9F40A0'
        case 'flying':
            return '#A790F0'
        case 'psychic':
            return '#F85888'
        case 'bug':
            return '#A8B721'
        case 'ground':
            return '#E0C068'
        case 'rock':
            return '#B8A038'
        case 'ghost':
            return '#6F5897'
        case 'dark':
            return '#705848'
        case 'dragon':
            return '#7138F8'
        case 'steel':
            return '#B8B8D0'
        case 'fairy':
            return '#EE99AC'
    }
}